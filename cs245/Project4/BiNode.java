

public class BiNode {
	private int degree;
	private int key;
	private int distance;
	private int path;
	private BiNode parent;
	private BiNode child;
	private BiNode sibling;
	
	public BiNode() {
		this.degree = 0;
		this.path = -1;
		this.parent = null;
		this.child = null;
		this.sibling = null;
	}
	
	public BiNode(int key, int distance) {
		this.degree = 0;
		this.key = key;
		this.distance = distance;
		this.path = -1;
		this.parent = null;
		this.child = null;
		this.sibling = null;
	}
	
	public void setDegree(int degree) {
		this.degree = degree;
	}
	
	public int degree() {
		return degree;
	}
	
	public void setKey(int key) {
		this.key = key;
	}
	
	public int key() {
		return key;
	}
	
	public void setDistance(int distance) {
		this.distance = distance;
	}
	
	public int distance() {
		return distance;
	}
	
	public void setPath(int path) {
		this.path = path;
	}
	
	public int path() {
		return path;
	}
	
	public void setParent(BiNode parent) {
		this.parent = parent;
	}
	
	public BiNode parent() {
		return parent;
	}
	
	public void setChild(BiNode child) {
		this.child = child;
	}
	
	public BiNode child() {
		return child;
	}
	
	public void setSibling(BiNode sibling) {
		this.sibling = sibling;
	}
	
	public BiNode sibling() {
		return sibling;
	}
}
